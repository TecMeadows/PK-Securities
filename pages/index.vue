        .then(res => {
          this.name = "";
          this.email = "";
          this.phone_number = "";
          this.address = "";
          $("#file").val("");

          alert("Query Successfully Sent");
        })
        .catch(res => {
          alert("Error on Query Submission");
        });
    },
